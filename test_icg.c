int main()
{
    int a;
    int b;
    int result;
    
    // Simple assignments
    a = 10;
    b = 5;
    
    // Arithmetic operation
    result = a + b;
    
    // Simple if condition
    if(result > 10)
    {
        result = result * 2;
    }
    
    // Simple while loop
    while(result > 0)
    {
        result = result - 1;
    }
} 